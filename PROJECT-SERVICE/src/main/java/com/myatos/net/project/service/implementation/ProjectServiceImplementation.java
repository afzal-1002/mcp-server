    @Override
    @Transactional
    public ProjectResponse updateProject(Long id, UpdateProjectRequest request) {
        Objects.requireNonNull(id, "id is required");
        Objects.requireNonNull(request, "request is required");

        final String baseUrl = currentCred().getBaseUrl();

        Project entity = projectRepository.findById(id)
                .orElseThrow(() -> new IllegalArgumentException("Project id not found: " + id));

        if (!isBlank(request.getProjectName()))    entity.setName(request.getProjectName().trim());
        if (!isBlank(request.getDescription()))    entity.setDescription(request.getDescription().trim());
        if (!isBlank(request.getProjectTypeKey())) entity.setProjectTypeKey(request.getProjectTypeKey().trim().toLowerCase());
        entity.setBaseUrl(baseUrl);

        if (!isBlank(request.getLeadAccountId())) {
            addOrUpdateLeadLink(entity, request.getLeadAccountId().trim());
        }

        Project saved = projectRepository.saveAndFlush(entity);
        String keyOrId = !isBlank(saved.getKey()) ? saved.getKey() : saved.getJiraId();
        if (isBlank(keyOrId)) {
            return projectMapper.fromProjectToResponse(saved);
        }

        Map<String, Object> body = new HashMap<>();
        if (!isBlank(saved.getName()))        body.put("name", saved.getName());
        body.put("description", nullToEmpty(saved.getDescription()));
        if (!isBlank(saved.getLeadUserId()))  body.put("leadAccountId", saved.getLeadUserId());

        UserCredentialResponse cred = currentCred();
        callConfiguredJira(baseUrl, "PUT", "PROJECT_ID_OR_KEY", cred.getUsername(), cred.getToken(), body, JsonNode.class, keyOrId);

        JiraProjectResponse jira = fetchFullProject(baseUrl, keyOrId, cred.getUsername(), cred.getToken());
        return persistFromJira(jira);
    }

    @Override
    @Transactional
    public ProjectResponse updateProjectByKey(String projectKey, UpdateProjectRequest request) {
        Objects.requireNonNull(request, "request is required");
        final String baseUrl = currentCred().getBaseUrl();
        final String key = requireNotBlank(projectKey, "projectKey required").trim().toUpperCase();

        Project entity = projectRepository.findByKeyAndBaseUrl(key, baseUrl)
                .orElseThrow(() -> new IllegalArgumentException("Project not found by key: " + key));

        return updateProject(entity.getId(), request);
    }

