    @Override
    @Transactional
    public List<ProjectResponse> syncAllProjectsFromJira() {
        final UserCredentialResponse cred = currentCred();
        final String baseUrl = normalizeBaseUrl(cred.getBaseUrl());

        List<JiraProjectResponse> jiraProjects = getAllProjectsFromJira(baseUrl);
        List<ProjectResponse> out = new ArrayList<>(jiraProjects.size());
        Set<String> jiraKeys = new HashSet<>();
        Set<String> jiraIds = new HashSet<>();
        for (JiraProjectResponse jp : jiraProjects) {
            if (!isBlank(jp.getKey())) {
                jiraKeys.add(jp.getKey().trim().toUpperCase());
            }
            if (!isBlank(jp.getId())) {
                jiraIds.add(jp.getId().trim());
            }
            out.add(persistFromJira(jp));
        }

        List<Project> locals = projectRepository.findAllByBaseUrl(baseUrl);
        for (Project local : locals) {
            String localKey = isBlank(local.getKey()) ? null : local.getKey().trim().toUpperCase();
            String localJiraId = isBlank(local.getJiraId()) ? null : local.getJiraId().trim();
            boolean existsInJira = (localKey != null && jiraKeys.contains(localKey))
                    || (localJiraId != null && jiraIds.contains(localJiraId));
            if (!existsInJira) {
                projectRepository.delete(local);
            }
        }
        return out;
    }

